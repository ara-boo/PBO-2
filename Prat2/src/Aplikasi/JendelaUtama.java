    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = tblData.getSelectedRow();
        if (selectedRow!=-1) {
            if(btnDelete.getText().equals("Hapus")){
                int respon = JOptionPane.showConfirmDialog(this,"Beneran Mau Dihapus?","Konfirmasi",JOptionPane.YES_NO_OPTION);

                if (respon == 0){
                    String NoBP = tfNoBP.getText();
                    String Nama = tfNama.getText();

                    if (NoBP.isEmpty()|| Nama.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "kolom harus diisi", "informasi", JOptionPane.ERROR_MESSAGE);
                        return;
                    }

                    try{
                        String cmdSql = "Delete from tbl_mahasiswa where No_BP = ?;";
                        PreparedStatement preStat = coon.prepareStatement(cmdSql);

                        preStat.setString(1,NoBP);

                        int RowInsert = preStat.executeUpdate();
                        if (RowInsert >0 ){
                            JOptionPane.showMessageDialog(this,"Data Berhasil Dihapus","informasi",JOptionPane.INFORMATION_MESSAGE);
                            resetForm();
                            getData();

                        } else{
                            JOptionPane.showMessageDialog(this,"Gagal Menghapus Data","informasi",JOptionPane.ERROR_MESSAGE);
                        }

                        preStat.close();

                    } catch  (Exception e){
                        JOptionPane.showMessageDialog(this,"Terjadi Kesalahan:" + e.getMessage(), "Error",JOptionPane.ERROR_MESSAGE);
                        Logger.getLogger(JendelaUtama.class.getName()).log(Level.SEVERE,null,e);
                    }

                    resetForm();
                }
            }
        }
    }

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = tblData.getSelectedRow();
        if (selectedRow!=-1){

            if (btnEdit.getText().equals("Edit")){
                tfNoBP.setEditable(true);
                tfNama.setEditable(true);
                tfTmtLahir.setEditable(true);
                tfTglLahir.setEditable(true);
                taAlamat.setEditable(true);
                tfTelpon.setEditable(true);
                tfAsalSekolah.setEditable(true);
                btnEdit.setText("Simpan");
            } else if (btnEdit.getText().equals("Simpan")){
                String NoBP = tfNoBP.getText();
                String Nama = tfNama.getText();
                String TmtLahir = tfTmtLahir.getText();
                String TglLahir = tfTglLahir.getText();
                String Alamat = taAlamat.getText();
                String Telpon = tfTelpon.getText();
                String AsalSekolah = tfAsalSekolah.getText();

                if (NoBP.isEmpty()|| Nama.isEmpty()){
                    JOptionPane.showMessageDialog(this,"kolom harus diisi","informasi",JOptionPane.ERROR_MESSAGE);
                    return;
                }

                try{
                    String cmdSql = "update tbl_mahasiswa set Nama = ?, Tempat_Lahir = ?, Tanggal_Lahir = ?, Alamat = ?, Telp = ?, Asal_Sekolah = ? where No_BP = ?;";
                    PreparedStatement preStat = coon.prepareStatement(cmdSql);

                    preStat.setString(1, Nama);
                    preStat.setString(2, TmtLahir);
                    preStat.setString(3, TglLahir);
                    preStat .setString(4, Alamat);
                    preStat.setString(5, Telpon);
                    preStat.setString(6, AsalSekolah);
                    preStat.setString(7, NoBP);

                    int rowInsert = preStat.executeUpdate();
                    if (rowInsert > 0){
                        JOptionPane.showMessageDialog(this, "Data Update Berhasil Disimpan","informasi", JOptionPane.INFORMATION_MESSAGE);
                        resetForm();
                        getData();

                    } else{
                        JOptionPane.showMessageDialog(this, "Gagal Meng-update data","Erro",JOptionPane.ERROR_MESSAGE);
                    }
                    preStat.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this,"Terjadi Kesalahan:" + e.getMessage(), "Error",JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(JendelaUtama.class.getName()).log(Level.SEVERE,null,e);
                }

                btnEdit.setText("Edit");
                resetForm();

            }

        } else{
            JOptionPane.showMessageDialog(this, "pilih data terlebih dahulu dari tabel","informasi",JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        String NoBP = tfNoBP.getText();
        String nama = tfNama.getText();
        String TmtLahir = tfTmtLahir.getText();
        String TglLahir = tfTglLahir.getText();
        String Alamat = taAlamat.getText();
        String Telpon = tfTelpon.getText();
        String AsalSekolah =tfAsalSekolah.getText();

        if(NoBP.isEmpty() || nama.isEmpty()){
            JOptionPane.showMessageDialog(this, "Semua Kolom Harus Diisi","Validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try{
            String sql = "INSERT INTO tbl_mahasiswa (No_BP, Nama, Tempat_Lahir, Tanggal_Lahir, Alamat, Telp, Asal_Sekolah) values (?,?,?,?,?,?,?)";
            PreparedStatement st = coon.prepareStatement(sql);

            st.setString(1,NoBP);
            st.setString(2,nama);
            st.setString(3,TmtLahir);
            st.setString(4,TglLahir);
            st.setString(5,Alamat);
            st.setString(6,Telpon);
            st.setString(7,AsalSekolah);

            int rowInserted = st.executeUpdate();
            if(rowInserted >0){
                JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan");
                resetForm();
                getData();
            }
            st.close();
        } catch (Exception e) {
            Logger.getLogger(JendelaUtama.class.getName()).log(Level.SEVERE,null,e);

        }
    }//GEN-LAST:event_btnNewActionPerformed

