    private void getData(){
        DefaultTableModel model = (DefaultTableModel) tblData.getModel();
        model.setRowCount(0);
        
        try{
            String sql = "select* from tbl_mahasiswa";
            PreparedStatement st = coon.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            
            
            SimpleDateFormat DF = new SimpleDateFormat("yyyy-MM-dd");
            while(rs.next()){
                String NoBP = rs.getString("No_BP");
                String Nama = rs.getString("Nama");
                String TmtLahir = rs.getString("Tempat_Lahir");
                String Alamat = rs.getString("Alamat");
                String Telpon = rs.getString("Telp");
                String AsalSekolah = rs.getString("Asal_Sekolah");

                String Tgl =rs.getString("Tanggal_Lahir");
                Date TglLahir = null;

                if(TglLahir != null){
                   TglLahir = new Date(DF.parse(Tgl).getTime());
               }
                Object[] rowData = {NoBP,Nama,TmtLahir,Tgl,Alamat,Telpon,AsalSekolah};
                model.addRow(rowData);
            }   
            rs.close();
            st.close();
        }catch (Exception e) {
            Logger.getLogger(JendelaUtama.class.getName()).log(Level.SEVERE,null,e);
        }
    }
